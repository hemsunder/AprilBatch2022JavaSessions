package OOP_Encapsulation;

public class EncapsulationConstructor{
	
	private String name;
	private int age;
	
	
	public EncapsulationConstructor(String name, int age) {
		this.name = name;
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}
	
	

}
